#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - adds
 * @a: num1
 * @b: num2
 * Return: answer
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substracts two nums
 * @a: num1
 * @b: num2
 * Return: answer
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies two num2
 * @a: num1
 * @b: num2
 * Return: answer
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div -divides two nums
 * @a: num1
 * @b: num2
 * Return: answer
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - remainder after division
 * @a: num1
 * @b: num2
 * Return: answer
*/

int op_mod(int a, int b)
{
return (a % b);
}
